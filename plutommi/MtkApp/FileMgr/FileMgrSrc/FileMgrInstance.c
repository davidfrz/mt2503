    /* Code Body                                                      */
    /*----------------------------------------------------------------*/

    /* Default value */
    if (string_id)
    {
        *string_id = 0;
    }

    if (icon_id)
    {
        *icon_id = 0;
    }

    instance = fmgr_get_instance_by_id(instance_id);

    /* Use (app_id) setting */
