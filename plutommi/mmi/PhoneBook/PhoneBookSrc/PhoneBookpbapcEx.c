#endif
                        
    MMI_TRACE(MMI_PHB_PBAPC_TRACE_GROUP, TRC_MMI_PHB_PBAPC_SHOW_NUM,
        g_mmi_phb_pbapc_cntx.contact_count, g_mmi_phb_pbapc_cntx.pse_phone_num, g_mmi_phb_pbapc_cntx.pse_sim_num);
