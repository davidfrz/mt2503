VMINT vm_set_time(vm_time_t* time)
{
	RTC_CTRL_SET_TIME_T rtctime;
    DCL_HANDLE hRtc;

    hRtc = DclRTC_Open(DCL_RTC, FLAGS_NONE);
    if (hRtc == DCL_HANDLE_INVALID || time == NULL)
    {
        return -1;
    }
	
	rtctime.u1Day = time->day;
	rtctime.u1Hour = time->hour;
	rtctime.u1Min = time->min;
	rtctime.u1Mon = time->mon;
	rtctime.u1Sec = time->sec;
	rtctime.u1Year = time->year - YEARFORMATE;
	rtctime.u1WDay = applib_dt_dow(rtctime.u1Year, rtctime.u1Mon, rtctime.u1Day);

    DclRTC_Control(hRtc, RTC_CMD_SET_TIME, (DCL_CTRL_DATA_T *)&rtctime);
    DclRTC_Close(hRtc);
	return 0;
}

