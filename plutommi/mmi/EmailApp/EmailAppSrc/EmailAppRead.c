                if (result != SRV_EMAIL_RESULT_SUCC)
                {
                    srv_email_msg_destroy(msg_handle);
                    mmi_email_util_display_error_popup(app_data->grp_id, result);
                    return;
                }
                result = srv_email_msg_set_flag(msg_handle, EMAIL_MSG_FLAG_SEEN, 0);
