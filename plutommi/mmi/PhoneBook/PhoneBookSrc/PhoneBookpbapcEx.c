    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    mmi_phb_pbapc_cntx_struct *phb_pbapc_cntx_p = NULL;
    S32 index = -1;
    mmi_phb_pbapc_sel_num_rsp_struct *sel_num_rsp;
    S32 ret = 0;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
    phb_pbapc_cntx_p = mmi_phb_pbapc_get_cntx();
    sel_num_rsp = (mmi_phb_pbapc_sel_num_rsp_struct *) (cb_info->type_rsp);

