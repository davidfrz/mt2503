                ShowString_n_baseline(bs->xtx, bs->yty + EDITOR_LINE_UPPER_PIXTEL, *UI_font, 0, (U8*)&current_character, 1, g_editor_line.line_ascent - EDITOR_LINE_UPPER_PIXTEL);
