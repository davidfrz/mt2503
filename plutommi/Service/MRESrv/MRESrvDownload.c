/*****************************************************************************
* FUNCTION
*  srv_mre_da_get_app_id
* DESCRIPTION
*  MRE service, data account , to get a app ID
* PARAMETERS
*  void
* RETURNS
*  Application ID
*****************************************************************************/
U8 srv_mre_da_get_app_id(srv_mre_da_type da_type)
{
#ifdef __MMI_MRE_DTCNT_SETTING__

    U8 app_id = 0;
#ifdef __MRE_SAL_SOCKET__
    U32 apn_id = 0;

    switch(da_type)
    {
    case SRV_MRE_DA_SIM1:
    case SRV_MRE_DA_SIM2:
    case SRV_MRE_DA_SIM3:
    case SRV_MRE_DA_SIM4:
        apn_id = VM_TCP_APN_CMNET;
        break;

    case SRV_MRE_DA_SIM1_PX:
    case SRV_MRE_DA_SIM2_PX:
    case SRV_MRE_DA_SIM3_PX:
    case SRV_MRE_DA_SIM4_PX:
        apn_id = VM_TCP_APN_CMWAP;
        break;

    default:
        break;
    }

    vm_get_app_id(apn_id, &app_id);
#endif 
    return app_id;
#else
    return 0;
#endif /* __MMI_MRE_DTCNT_SETTING__ */
}


/*****************************************************************************
* FUNCTION
*  srv_mre_da_get_account_id
* DESCRIPTION
*  MRE service, data account , to get a data account ID from NVRAM, could use this interface
* PARAMETERS
*  da_type         [IN]        The type of MRE Data account want to get
* RETURNS
*  Data Account ID of needed type.
*****************************************************************************/
U32 srv_mre_da_get_account_id(srv_mre_da_type da_type)
{
#ifdef __MMI_MRE_DTCNT_SETTING__
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    U32 temp[2] = {0,0};
    S16 error;
#ifdef __COSMOS_MMI_PACKAGE__    
    U32 apn_id = 0;
    U8 app_id = 0;
    cbm_sim_id_enum sim_id = CBM_SIM_ID_SIM1;
#endif /* #ifdef __COSMOS_MMI_PACKAGE__ */    
    
    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/    
#ifdef __MRE_SAL_SOCKET__
    switch(da_type)
    {
    case SRV_MRE_DA_SIM1:
        ReadValue(NVRAM_MRE_SRV_DA_SIM1, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM1_PX:
        ReadValue(NVRAM_MRE_SRV_DA_SIM1_PX, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM2:
        ReadValue(NVRAM_MRE_SRV_DA_SIM2, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM2_PX:
        ReadValue(NVRAM_MRE_SRV_DA_SIM2_PX, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM3:
        ReadValue(NVRAM_MRE_SRV_DA_SIM3, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM3_PX:
        ReadValue(NVRAM_MRE_SRV_DA_SIM3_PX, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM4:
        ReadValue(NVRAM_MRE_SRV_DA_SIM4, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM4_PX:
        ReadValue(NVRAM_MRE_SRV_DA_SIM4_PX, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_WIFI_ONLY:
        ReadValue(NVRAM_MRE_SRV_DA_IS_WIFI_ONLY, temp, DS_BYTE, &error);
        break;

    default:
        break;
    }
    
#ifdef __COSMOS_MMI_PACKAGE__
    switch(da_type)
    {
    case SRV_MRE_DA_SIM1:
        sim_id = CBM_SIM_ID_SIM1;
        apn_id = VM_TCP_APN_CMNET;
        break;

    case SRV_MRE_DA_SIM1_PX:
        sim_id = CBM_SIM_ID_SIM1;
        apn_id = VM_TCP_APN_CMWAP;
        break;

    case SRV_MRE_DA_SIM2:
        sim_id = CBM_SIM_ID_SIM2;
        apn_id = VM_TCP_APN_CMNET;
        break;

    case SRV_MRE_DA_SIM2_PX:
        sim_id = CBM_SIM_ID_SIM2;
        apn_id = VM_TCP_APN_CMWAP;
        break;


    case SRV_MRE_DA_SIM3:
        sim_id = CBM_SIM_ID_SIM3;
        apn_id = VM_TCP_APN_CMNET;
        break;

    case SRV_MRE_DA_SIM3_PX:
        sim_id = CBM_SIM_ID_SIM3;
        apn_id = VM_TCP_APN_CMWAP;
        break;


    case SRV_MRE_DA_SIM4:
        sim_id = CBM_SIM_ID_SIM4;
        apn_id = VM_TCP_APN_CMNET;
        break;

    case SRV_MRE_DA_SIM4_PX:
        sim_id = CBM_SIM_ID_SIM4;
        apn_id = VM_TCP_APN_CMWAP;
        break;

    case SRV_MRE_DA_WIFI_ONLY:
        break;

    default:
        break;
    }

    /* Check if 0xFFFFFFFF, it's a init number of NVRAM, need to use automatic */
    if ((temp[0] == 0xFFFFFFFF) && (da_type != SRV_MRE_DA_WIFI_ONLY))
    {
        vm_get_app_id(apn_id, &app_id);
        temp[0] = cbm_encode_data_account_id((kal_uint32)VAPP_DTCNT_AUTOMATIC_ID, sim_id, app_id, 0);
    }
#endif /* __COSMOS_MMI_PACKAGE__ */

#endif /*__MRE_SAL_SOCKET__ */
    return (U32)temp[0];
#else
    return 0;
#endif /*__MMI_MRE_DTCNT_SETTING__ */
}


