VMINT vm_update_check_launch(VMWSTR path, VMWSTR new_path)
{
    VMINT fh;
    VMINT length,i = 1;
    static const VMWCHAR suffix[][4]=
    {
        {L'v',L'x',L'p',0},
        {L'v',L'p',L'p',0},
        {L'v',L's',L'p',0},
        {0,0,0,0},
    };
    if (!new_path || !path)
    {
        return FALSE;
    }
    
    if (vm_update_can_be_replaced(path))
    {
        return FALSE;
    }

    vm_update_get_path(new_path,path,1);
    length = kal_wstrlen(new_path)-3;

    while (suffix[i][0])
    {
        kal_wstrcat(new_path,(const WCHAR *)suffix[i]);

        fh = FS_Open(new_path,FS_READ_ONLY);
        if (fh >= 0)
        {
            FS_Close(fh);
            return TRUE;
        }
        i++;
    }
    
    vm_update_get_path(new_path,path,0);
    length = kal_wstrlen(new_path)-3;
    i = 1;
    
    while (suffix[i][0])
    {
        kal_wstrcat(new_path,(const WCHAR *)suffix[i]);

        fh = FS_Open(new_path,FS_READ_ONLY);
        if (fh >= 0)
        {
            FS_Close(fh);
            return TRUE;
        }
        i++;
    }
    return FALSE;
}

