/*
        How to add message id in KAL.
        Since W10.22, the add message id method is greatly simplified.
        You only modify mcu/adaptation/include/user_msgid.h as the template.
 
        I) The template
        #ifdef KAL_MSGID_EXPANSION
            #include "message_sap.h"
            MSG_ID_NAME(MSG_NAME)
        #else
            MSG_ID_RANGE(MSG_NAME, MSG_RANGE)
        #endif

        II)Discription of the template:
        You should follow the template, and only modify the following fields.
        1)  message_sap.h: this is your message ids header file
        2)  MSG_NAME: this is a name for your range of message ids, e.g, MMI, SMS
        3)  MSG_RANGE: this is the limit number of your message ids

        III) Behind the template:
        MSG_ID_RANGE(MSG_NAME, MSG_RANGE) will expand MSG_NAME into MSG_ID_##MSG_NAME##_CODE_BEGIN.
        You should use MSG_ID_##MSG_NAME##_CODE_BEGIN as the base message id in your message_sap.h
*/

