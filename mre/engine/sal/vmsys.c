static void vm_update_check_folder(VMWSTR app_path)
{
    VMWSTR p;
    
    p = app_path + kal_wstrlen(app_path)-1;
    if (*app_path != vm_get_removeable_driver() && *app_path != vm_get_system_driver())
    {
        *app_path = 0;
        return;
    }
    
    while (p != app_path)
    {
        if (*p == '\\')
        {
            *p = 0;
            return;
        }
        p--;
    }
    *app_path = 0;
}
