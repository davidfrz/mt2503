    evt.menu_cui_evt = menu_cui_event;
    mmi_frm_group_send_to_caller(group_id, (mmi_group_event_struct*)&evt);

    if (file_info != NULL)
    {
        OslMfree(file_info);
        file_info = NULL;
    }
