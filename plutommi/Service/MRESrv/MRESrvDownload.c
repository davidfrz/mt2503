    S16 error;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/   
    temp[0] = da_id;
    switch(da_type)
    {
    case SRV_MRE_DA_SIM1:
        WriteValue(NVRAM_MRE_SRV_DA_SIM1, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM1_PX:
        WriteValue(NVRAM_MRE_SRV_DA_SIM1_PX, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM2:
        WriteValue(NVRAM_MRE_SRV_DA_SIM2, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM2_PX:
        WriteValue(NVRAM_MRE_SRV_DA_SIM2_PX, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM3:
        WriteValue(NVRAM_MRE_SRV_DA_SIM3, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM3_PX:
        WriteValue(NVRAM_MRE_SRV_DA_SIM3_PX, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM4:
        WriteValue(NVRAM_MRE_SRV_DA_SIM4, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_SIM4_PX:
        WriteValue(NVRAM_MRE_SRV_DA_SIM4_PX, temp, DS_DOUBLE, &error);
        break;

    case SRV_MRE_DA_WIFI_ONLY:
        WriteValue(NVRAM_MRE_SRV_DA_IS_WIFI_ONLY, &da_id, DS_BYTE, &error);
        break;

    default:
        break;
    }
#endif /* __MMI_MRE_DTCNT_SETTING__ */
