/*****************************************************************************
 * FUNCTION
 *  mmi_em_rf_hex_to_i
 * DESCRIPTION
 *  transfer a hex string to int value, don't process negative number
 * PARAMETERS
 *  buf         [IN]  hex string
 *  int_value   [IN]  pointer to return the transfer int value
 * RETURNS
 *  MMI_BOOL    if success  
 *****************************************************************************/
static MMI_BOOL mmi_em_rf_hex_to_i(U16 *buf, U32 *int_value)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    U32 temp;
    U32 value = 0;
    
    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
    while(*buf)
    {
        if( *buf >='0' && *buf <='9' )           /* 0 ~ 9 */
            temp = *buf - '0';
        else if (*buf >= 'A' && *buf <= 'F')     /* A ~ F */
            temp = *buf - 'A' + 10; 
        else if (*buf >= 'a' && *buf <= 'f')     /* a ~ f */
            temp = *buf - 'a' + 10;        
        else 
            return MMI_FALSE;
        value = value * 16 + temp;
        buf ++;
    }
    *int_value = value;
    return MMI_TRUE;    
}


