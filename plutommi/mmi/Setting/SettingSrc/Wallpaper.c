/*****************************************************************************
 * FUNCTION
 *  ExitPhnsetWallpaperSysView
 * DESCRIPTION
 *  This function is Exit function for preview wallpaper
 *  for Setting->Phonesetup->IdleScreenDisplay->Wallpaper (->Fix) ->System/Download -> View
 *  Functionality: Add to history
 * PARAMETERS
 *  void
 *  a(?)        [IN/OUT]        Void
 *  b(?)        [IN]            Void
 * RETURNS
 *  void
 *****************************************************************************/
