{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    ft_in_proc_call_req_struct *msg_p = (ft_in_proc_call_req_struct*) ilm_ptr->local_para_ptr;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
    ASSERT(msg_p->func != NULL);
    msg_p->func(msg_p->func_arg1, msg_p->func_arg2);
}
