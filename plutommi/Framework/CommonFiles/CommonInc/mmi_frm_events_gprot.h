/*
 * The follow MMI_RET_ERR_XXX is the error return value of the callback manager
 * Can't find deregistered callback and user data 
 */
#define MMI_RET_ERR_NO_CB_AND_DATA           -501
/* The designated callback with designated user data has been registered */
#define MMI_RET_ERR_HAS_BEEN_REGISTERED      -502
/* The designated callback with designated user data has not been registered */
#define MMI_RET_ERR_HAS_NOT_BEEN_REGISTERED  -503
/* Parameter is wrong */
#define MMI_RET_ERR_WRONG_PARA               -504
/* The designated event does not exist */
#define MMI_RET_ERR_CANT_FIND_EVT            -505
/* No callback function is registered */
#define MMI_RET_ERR_NO_REG_CB                -506
/* The post event is not recognised */
#define MMI_RET_ERR_UNKNOWN_EVT_FLAG         -507
/* Deregister action is reentered and has been posted */
#define MMI_RET_ERR_POST_DEREG_CB            -508
/* No post events */
#define MMI_RET_ERR_NO_POST_EVENT            -508

