        case EVT_ID_CUI_MENU_CLOSE_REQUEST:
        {
            cui_menu_close(menu_evt->sender_id);
            ret = MMI_RET_OK;
            break;
        }
        case EVT_ID_PHB_SAVE_CONTACT:
